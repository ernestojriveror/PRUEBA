/*5. Um cavaleiro precisa subir 5 degraus para entrar no castelo.
Crie um programa que mostre:
● Subindo degrau 1
● Subindo degrau 2
● ...
● Subindo degrau 5*/

let degrau = 5;

switch (degrau) {
    
    case 1: 
    console.log('Subindo degrau 1');
    break;

    case 2: 
    console.log('Subindo degrau 2');
    break;

    case 3: 
    console.log('Subindo degrau 3');
    break;

    case 4: 
    console.log('Subindo degrau 4');
    break;

    case 5: 
    console.log('Subindo degrau 5');
    break;

    default:
       console.log('Não pode subir'); 
}